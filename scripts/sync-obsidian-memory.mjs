#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const vaultRoot = path.join(root, 'obsidian-vault');
const engineDir = path.join(vaultRoot, 'Engine');
const templatesDir = path.join(vaultRoot, 'Templates');
const obsidianDir = path.join(vaultRoot, '.obsidian');
const workingBehaviorsPath = path.join(root, '.github', 'WORKING_BEHAVIORS.md');
const sessionLogPath = path.join(root, '.github', 'session-memory', 'session-log.jsonl');

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function writeFile(filePath, content) {
  ensureDir(path.dirname(filePath));
  fs.writeFileSync(filePath, content, 'utf8');
}

function readText(filePath) {
  return fs.readFileSync(filePath, 'utf8');
}

function parseSections(markdown) {
  const lines = markdown.split(/\r?\n/);
  const sections = [];
  for (const line of lines) {
    if (line.startsWith('## ')) {
      sections.push(line.replace(/^##\s+/, '').trim());
    }
  }
  return sections;
}

function formatSessionEvent(event) {
  const ts = event.ts || 'unknown-time';
  const action = (event.action || '').replace(/\|/g, '\\|').trim() || '(no action)';
  const outcome = (event.outcome || '').replace(/\|/g, '\\|').trim() || '(no outcome)';
  const surprise = typeof event.surprise === 'number' ? event.surprise.toFixed(2) : 'n/a';
  return `| ${ts} | ${action} | ${outcome} | ${surprise} |`;
}

function parseSessionLog(jsonl) {
  const events = [];
  for (const line of jsonl.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed) {
      continue;
    }
    try {
      events.push(JSON.parse(trimmed));
    } catch {
      // Keep export resilient even if one row is malformed.
    }
  }
  return events;
}

function buildWorkingBehaviorsNote(sourceMarkdown, sections) {
  const now = new Date().toISOString();
  return [
    '---',
    `source: ../.github/WORKING_BEHAVIORS.md`,
    `generatedAt: ${now}`,
    `sectionCount: ${sections.length}`,
    '---',
    '',
    '# Working Behaviors',
    '',
    'This note mirrors the repository contract and is regenerated automatically.',
    '',
    '## Section Index',
    '',
    ...sections.map((section) => `- ${section}`),
    '',
    '## Source Contract',
    '',
    sourceMarkdown,
    '',
  ].join('\n');
}

function buildSessionMemoryNote(events) {
  const now = new Date().toISOString();
  const latest = events.slice(-80).reverse();
  const tableRows = latest.map(formatSessionEvent);

  return [
    '---',
    `source: ../.github/session-memory/session-log.jsonl`,
    `generatedAt: ${now}`,
    `eventCount: ${events.length}`,
    '---',
    '',
    '# Session Memory',
    '',
    'Auto-export of session events for Obsidian review.',
    '',
    '## Recent Events',
    '',
    '| Time | Action | Outcome | Surprise |',
    '| --- | --- | --- | --- |',
    ...(tableRows.length > 0 ? tableRows : ['| (none) | (none) | (none) | (none) |']),
    '',
  ].join('\n');
}

